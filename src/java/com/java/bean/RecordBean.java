package com.java.bean;

import java.util.Date;

public class RecordBean {

    private int id;
    private String name;
    private String jailcity;
    private String gender;
    private Date dob;
    private String crime;
    private Date imprisondate;
    private Date releasedate;
    private String hometown;
    private String familynumber;
    private String lawyername;
    private String lawyernumber;

    public RecordBean() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getJailcity() {
        return jailcity;
    }

    public void setJailcity(String jailcity) {
        this.jailcity = jailcity;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public String getCrime() {
        return crime;
    }

    public void setCrime(String crime) {
        this.crime = crime;
    }

    public Date getImprisondate() {
        return imprisondate;
    }

    public void setImprisondate(Date imprisondate) {
        this.imprisondate = imprisondate;
    }

    public Date getReleasedate() {
        return releasedate;
    }

    public void setReleasedate(Date releasedate) {
        this.releasedate = releasedate;
    }

    public String getHometown() {
        return hometown;
    }

    public void setHometown(String hometown) {
        this.hometown = hometown;
    }

    public String getFamilynumber() {
        return familynumber;
    }

    public void setFamilynumber(String familynumber) {
        this.familynumber = familynumber;
    }

    public String getLawyername() {
        return lawyername;
    }

    public void setLawyername(String lawyername) {
        this.lawyername = lawyername;
    }

    public String getLawyernumber() {
        return lawyernumber;
    }

    public void setLawyernumber(String lawyernumber) {
        this.lawyernumber = lawyernumber;
    }

    
}